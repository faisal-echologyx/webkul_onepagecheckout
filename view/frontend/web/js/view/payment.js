define(
    [
        'jquery',
        "underscore",
        'uiComponent',
        'ko',
        'Webkul_OneStepCheckout/js/model/quote',
        'Magento_Checkout/js/model/step-navigator',
        'Magento_Checkout/js/model/payment-service',
        'Magento_Checkout/js/model/payment/method-converter',
        'Magento_Checkout/js/action/get-payment-information',
        'Magento_Checkout/js/model/checkout-data-resolver',
        'Webkul_OneStepCheckout/js/model/payment-loader'
    ],
    function (
        $,
        _,
        Component,
        ko,
        quote,
        stepNavigator,
        paymentService,
        methodConverter,
        getPaymentInformation,
        checkoutDataResolver,
        paymentLoader
    ) {
        'use strict';

        /** Set payment methods to collection */
        paymentService.setPaymentMethods(methodConverter(window.checkoutConfig.paymentMethods));

        return Component.extend({
            defaults: {
                template: 'Webkul_OneStepCheckout/payment',
                activeMethod: ''
            },
            isLoading: paymentLoader.isLoading,
            isVisible: true,
            isAvailable:window.checkoutConfig.paymentMethods.length>0,
            quoteIsVirtual: quote.quoteVirtual,
            isPaymentMethodsAvailable: ko.computed(function () {
                return paymentService.getAvailablePaymentMethods().length > 0;
            }),

            initialize: function () {
                this._super();
                checkoutDataResolver.resolvePaymentMethod();
                return this;
            },

            getFormKey: function () {
                return window.checkoutConfig.formKey;
            }
        });
    }
);
